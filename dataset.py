import pandas as pd
import numpy as np
import matplotlib.pyplot as plt

# Load dataset
dataset_path = "/content/drive/MyDrive/data_file.csv"
df = pd.read_csv(dataset_path)

# Basic info
print("<<<<<<<<<<<<<<<<<<<<<<<<<<<<HEAD>>>>>>>>>>>>>>>>>>>>>>>>>>>>")
print(df.head())
print("<<<<<<<<<<<<<<<<<<<<<<<<<<<<SHAPE>>>>>>>>>>>>>>>>>>>>>>>>>>>>")
print(df.shape)
print("<<<<<<<<<<<<<<<<<<<<<<<<<<<<INFO>>>>>>>>>>>>>>>>>>>>>>>>>>>>")
print(df.info())
print("<<<<<<<<<<<<<<<<<<<<<<<<<<<<DESCRIBE>>>>>>>>>>>>>>>>>>>>>>>>>>>>")
print(df.describe())
print("<<<<<<<<<<<<<<<<<<<<<<<<<<<<COLUMNS>>>>>>>>>>>>>>>>>>>>>>>>>>>>")
print(df.columns)

# ===================== HISTOGRAM =====================
df.hist(bins=50, figsize=(20, 15))
plt.suptitle("Histogram of All Numeric Columns")
plt.show()

# ===================== PIE CHART =====================
# Example: Show distribution of a categorical column (like 'Gender' or 'Category')
if 'Gender' in df.columns:
    gender_counts = df['Gender'].value_counts()
    plt.figure(figsize=(6, 6))
    plt.pie(gender_counts, labels=gender_counts.index, autopct='%1.1f%%', startangle=140)
    plt.title("Gender Distribution (Pie Chart)")
    plt.show()

# ===================== LINE CHART =====================
# Example: Plot a trend between two numeric columns
numeric_cols = df.select_dtypes(include=[np.number]).columns
if len(numeric_cols) >= 2:
    plt.figure(figsize=(8, 5))
    plt.plot(df[numeric_cols[0]], df[numeric_cols[1]], marker='o', linestyle='-', color='b')
    plt.title(f"Line Chart: {numeric_cols[0]} vs {numeric_cols[1]}")
    plt.xlabel(numeric_cols[0])
    plt.ylabel(numeric_cols[1])
    plt.grid(True)
    plt.show()

# ===================== BAR CHART =====================
# Example: Show average of a numeric column by a category
if 'Category' in df.columns and len(numeric_cols) > 0:
    category_mean = df.groupby('Category')[numeric_cols[0]].mean()
    plt.figure(figsize=(8, 5))
    category_mean.plot(kind='bar', color='teal')
    plt.title(f"Average {numeric_cols[0]} by Category (Bar Chart)")
    plt.xlabel('Category')
    plt.ylabel(f'Average {numeric_cols[0]}')
    plt.show()

